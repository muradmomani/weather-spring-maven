package com.progressoft.jipfive.station.requests;

public class StationLocationRequest {
	private double lat;
	private double Lang;

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLang() {
		return Lang;
	}

	public void setLang(double lang) {
		Lang = lang;
	}
}
